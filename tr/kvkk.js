function openFooterModal(type) {
  const modal = document.getElementById('footerModal');
  const title = document.getElementById('footer-modal-title');
  const body = document.getElementById('footer-modal-body');
  if (footerModalContents[type]) {
    title.textContent = footerModalContents[type].title;
    body.innerHTML = footerModalContents[type].content;
  } else {
    title.textContent = '';
    body.textContent = '';
  }
  modal.style.display = 'flex';
  document.body.style.overflow = 'hidden';
}

function closeFooterModal() {
  const modal = document.getElementById('footerModal');
  modal.style.display = 'none';
  document.body.style.overflow = '';
}

document.addEventListener('DOMContentLoaded', function () {
  // Linklere tıklama
  document.querySelectorAll('.footer-link').forEach(function (el) {
    el.addEventListener('click', function (e) {
      e.preventDefault();
      const type = el.getAttribute('data-modal');
      openFooterModal(type);
    });
  });
  // Modal kapama
  document.querySelectorAll('.footer-modal-close').forEach(function (el) {
    el.addEventListener('click', closeFooterModal);
  });
  // Modal dışında tıklayınca kapansın
  const modal = document.getElementById('footerModal');
  if (modal) {
    modal.addEventListener('click', function (e) {
      if (e.target === modal) closeFooterModal();
    });
  }
});
